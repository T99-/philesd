package com.t99sdevelopment.philesd.util;

public class FlagDataPair {
	
	public String flag;
	public String data;
	
	public FlagDataPair() {}

	public FlagDataPair(String flag) {

		this.flag = flag;

	}

	public FlagDataPair(String flag, String data) {

		this.flag = flag;
		this.data = data;

	}
	
}